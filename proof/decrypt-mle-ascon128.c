/**
 * This file DECRYPTS can decrypt any packet shown in
 * "ascon128-mle-decrypt-test.txt".
*/
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <inttypes.h>
#include <stdint.h>
#include <stdbool.h>

#include "ascon.h"
#include "common.h"

/**
 * From the file "ascon128-mle-decrypt-test.txt".
*/
void test1(void) {
  uint8_t key[] = {0x89, 0x0F, 0xF9, 0x74, 0x40, 0xEC, 0xF9, 0x66,
                   0x95, 0x87, 0x38, 0xE2, 0x15, 0x28, 0x03, 0x0B};

  uint8_t nonce[] = {0x0E, 0xA7, 0xEB, 0xDD, 0xA1, 0x63, 0xF4, 0x18,
                     0x0F, 0x3F, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00};

  uint8_t assocData[] = {0x0E, 0xA7, 0xEB, 0xDD, 0xA1, 0x63, 0xF4, 0x18,
                         0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01};

  uint8_t tag[] = {
    0x63, 0xFA, 0xFE, 0x2B 
  };

  uint8_t payload[] = {
    0xD1, 0x24, 0xF5, 0x27, 0x24, 0xC6, 0x55, 0x89,
    0x00, 0xF8, 0x75, 0xD8, 0xA1, 0x12, 0xCE, 0x0C,
    0xB9, 0xF4, 0x33, 0x5F, 0x2C, 0x73, 0xC8, 0xA2,
    0x23, 0x41, 0x0C
  };

  bool status = ascon_aead128_decrypt(payload, key, nonce, assocData, payload,
                                      tag, CRYPTO_ABYTES, sizeof(payload),
                                      sizeof(tag));
  if (status == ASCON_TAG_OK) {
    printf("Test 1 ASCON decryption success!\n");
  }
  else {
    printf("Test 1 ASCON decryption failure.\n");
  }
}

int main(void) {
  test1();
  return 0;
}
